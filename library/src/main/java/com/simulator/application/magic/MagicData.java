package com.simulator.application.magic;

public class MagicData {
    private MagicDataType type;
    private String name;
    private String value;

    public MagicDataType getType() {
        return type;
    }

    public void setType(MagicDataType type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
